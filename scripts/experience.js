$('#coding-button').click(function() {
    if ($('#cod-list').hasClass('hidden')) {
        $('#cod-list').removeClass('hidden');
      } else {
        $('#cod-list').addClass('hidden');
      }
});

$('#expr-button').click(function() {
    if ($('#expr-list').hasClass('hidden')) {
        $('#expr-list').removeClass('hidden');
      } else {
        $('#expr-list').addClass('hidden');
      }
});
